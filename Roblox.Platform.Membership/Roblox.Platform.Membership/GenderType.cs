namespace Roblox.Platform.Membership;

/// <summary>
/// Represents the gender of a user.
/// </summary>
public enum GenderType : byte
{
	/// <summary>
	/// The user's gender is unknown.
	/// </summary>
	Unknown = 1,
	/// <summary>
	/// The user is male.
	/// </summary>
	Male,
	/// <summary>
	/// The user is female.
	/// </summary>
	Female
}
